process.env.NODE_ENV = 'test';
import app from '../server/index.js'
import chai from 'chai'
import recipes from '../server/models/recipe.js'
import request from 'supertest'

const expect = chai.expect;
describe('GET count of all recipes', () => {
    it('it should GET count of recipes > 19', (done) => {
      request(app).get("/api/get-count-recipes")
      .end((err, res) => {
        expect(res.body).to.be.above(19);
        expect(res.statusCode).to.equal(200);
        done();
      });
    });
});